#Perulangan (loop)

#loop dengan list
print("FOR WITH LIST")
angka2_list = [1,2,3,4,5]
print(angka2_list)
for i in angka2_list:
    print(i)
print("akhir dari program \n")

#loop dengan range
print("FOR WITH RANGE")
angka2_range = range(5)
print(angka2_range)
for i in angka2_range:
    print(i)
print("\n")

angka3_range = range(1,10)
for a in angka3_range:
    print(a)
print("\n")

angka4_range = range(1,11)
for b in angka4_range:
    print("hello word")
print("akhir dari program \n")

#loop dengan str
print("FOR WITH STRING")
data_str = "hallo apa kabar"
for i in data_str:
    print(i)
print("akhir dari program")


    
